#!env bash
set -e
set -a
source /var/.env
set +a

psql -v ON_ERROR_STOP=1 <<-EOSQL
  CREATE DATABASE $PG_DATABASE;
  CREATE USER $PG_USERNAME;
  GRANT ALL PRIVILEGES ON DATABASE $PG_DATABASE TO $PG_USERNAME;
  ALTER DATABASE $PG_DATABASE OWNER TO $PG_USERNAME;
  ALTER USER postgres WITH PASSWORD '$PG_POSTGRES_PASSWORD';
  ALTER USER $PG_USERNAME with PASSWORD '$PG_PASSWORD';
EOSQL
